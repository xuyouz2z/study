# 横向制表符
print('1\t23')
# 纵向制表符
print('1\v23')
# 换行
print('12\n3')
# 输出带'的
print("Les's go")
print('Les\'s go')
# 原始字符串（需在引号之前添加r）
str1 = r'D:\Python\now'
print(str1)
# 注意：原始字符串末尾不能加反斜杠\
# print(r'D:\Python\now\')
str2 = r'D:\Python\now''\\'
print(str2)
print(r'D:\Python\now''\\')
# 三引号（无需换行）
print('''你好
世界
哈哈哈''')