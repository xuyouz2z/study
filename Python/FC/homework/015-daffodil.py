for i in range(100, 1000):
    # int对象不能包含下标 所以把i转换为str型 把下标单独取出来
    s = str(i)
    # 此时s='100's[0]=1  s[1]=0  s[2]=0
    # 单独取出来后，再转化为int型
    a = int(s[0])
    b = int(s[1])
    c = int(s[2])
    # 计算a,b,c的三次方的和
    if i == a**3 + b**3 + c**3:
        print(i)

# 计算水仙花数=三位数其各位数字的立方和等于该数本身
# 例如 153 = 1**3+5**3+3**3
